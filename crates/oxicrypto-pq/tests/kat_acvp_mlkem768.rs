    /// Deterministic keygen + encap must be reproducible across invocations.
    #[test]
    fn acvp_mlkem768_determinism_is_stable() {
        let dk_seed: [u8; 64] = [0x42u8; 64];
        let enc_seed: [u8; 32] = [0x43u8; 32];

        let (dk1, ek1) = MlKem768::generate_deterministic(&dk_seed);
        let (ct1, ss1_enc) = ek1
            .encapsulate_deterministic(&enc_seed)
            .expect("first encapsulate_deterministic failed");
        let ss1_dec = dk1.decapsulate(&ct1).expect("first decapsulate failed");

        let (dk2, ek2) = MlKem768::generate_deterministic(&dk_seed);
        let (ct2, ss2_enc) = ek2
            .encapsulate_deterministic(&enc_seed)
            .expect("second encapsulate_deterministic failed");
        let ss2_dec = dk2.decapsulate(&ct2).expect("second decapsulate failed");

        assert_eq!(
            ss1_enc.as_slice(),
            ss2_enc.as_slice(),
            "deterministic encap must produce identical SS on repeated calls"
        );
        assert_eq!(
            ss1_dec.as_slice(),
            ss2_dec.as_slice(),
            "deterministic decap must produce identical SS on repeated calls"
        );
        assert_eq!(
            ek1.to_bytes(),
            ek2.to_bytes(),
            "deterministic keygen must produce identical EK bytes"
        );
        assert_eq!(
            ct1.to_bytes(),
            ct2.to_bytes(),
            "deterministic encap must produce identical ciphertext bytes"
        );
    }
