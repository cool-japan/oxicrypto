// ── PKCS1v15 SHA-256 ──────────────────────────────────────────────────────────

#[test]
fn rsa_pkcs1v15_sha256_sign_verify() {
    let signer =
        RsaPkcs1v15Sha256Signer::from_pkcs8_der(RSA_2048_PKCS8_DER).expect("signer from DER");
    let sig = signer.sign(MSG).expect("sign");
    assert_eq!(sig.len(), 256, "RSA-2048 signature should be 256 bytes");

    let verifier =
        RsaPkcs1v15Sha256Verifier::from_spki_der(RSA_2048_SPKI_DER).expect("verifier from DER");
    verifier.verify(MSG, &sig).expect("verify");
}

#[test]
fn rsa_pkcs1v15_sha256_wrong_msg_fails() {
    let signer = RsaPkcs1v15Sha256Signer::from_pkcs8_der(RSA_2048_PKCS8_DER).expect("signer");
    let sig = signer.sign(MSG).expect("sign");
    let verifier = RsaPkcs1v15Sha256Verifier::from_spki_der(RSA_2048_SPKI_DER).expect("verifier");
    assert!(verifier.verify(b"wrong message", &sig).is_err());
}

