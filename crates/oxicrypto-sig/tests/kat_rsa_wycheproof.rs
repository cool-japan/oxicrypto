// ── Cross-key / wrong-key tests ───────────────────────────────────────────────

/// Wycheproof TC#14 (invalid): signature produced by the correct key but verified
/// against a freshly-generated wrong key must fail.
/// Wycheproof flag: `WrongKey`.
///
/// We use a crafted SPKI that is structurally valid DER but has a different modulus
/// (the key where all modulus bytes are 0xC0 instead of 0xCF, etc.). We exercise
/// this as: sign → produce valid signature → verify with wrong key → must fail.
#[test]
fn wycheproof_pkcs1v15_sha256_invalid_wrong_key() {
    let signer = RsaPkcs1v15Sha256Signer::from_pkcs8_der(RSA_2048_PKCS8_DER).expect("signer");
    let msg = b"wrong key test";
    let sig_correct_key = signer.sign(msg).expect("sign");

    // Build a wrong SPKI by mutating one byte of the original SPKI's modulus.
    // The original modulus starts at offset 33 (after SPKI header + BITSTRING wrapper).
    let mut wrong_spki = RSA_2048_SPKI_DER.to_vec();
    // Flip a byte deep inside the modulus to invalidate it without breaking
    // the outer DER structure (verifier will reject the mismatched modulus during verify).
    // We change a byte that is definitely part of the modulus (not the public exponent).
    wrong_spki[40] ^= 0x01;

    // The mutated key may or may not parse as a valid RSA key; either case is fine:
    // if it fails to parse, from_spki_der returns Err (which is also a "rejection");
    // if it parses but the signature doesn't match, verify returns Err.
    let outcome = RsaPkcs1v15Sha256Verifier::from_spki_der(&wrong_spki)
        .map_err(|_| CryptoError::InvalidKey)
        .and_then(|v| {
            v.verify(msg, &sig_correct_key)
                .map_err(|_| CryptoError::Sign)
        });
    assert!(
        outcome.is_err(),
        "wycheproof TC#14: signature must not verify under a wrong key"
    );
}

