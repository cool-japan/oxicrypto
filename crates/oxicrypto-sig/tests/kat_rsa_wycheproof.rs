// ── PSS non-determinism validation ───────────────────────────────────────────

/// Wycheproof PSS TC#9 (valid): PSS is randomized — two signatures of the same
/// message must be different (probabilistic test, failure probability ≈ 2^-256).
#[test]
fn wycheproof_pss_sha256_valid_nondeterministic() {
    let signer = RsaPssSha256Signer::from_pkcs8_der(RSA_2048_PKCS8_DER).expect("PSS signer");
    let msg = b"PSS randomness test";
    let sig1 = signer.sign(msg).expect("PSS sign 1");
    let sig2 = signer.sign(msg).expect("PSS sign 2");
    // PSS uses a random salt: two signatures of the same message must differ.
    // (The only way they could be equal is a catastrophic RNG failure.)
    assert_ne!(
        sig1, sig2,
        "wycheproof PSS TC#9: PSS signatures must be non-deterministic (random salt)"
    );
    // Both must still verify correctly.
    let verifier = RsaPssSha256Verifier::from_spki_der(RSA_2048_SPKI_DER).expect("PSS verifier");
    verifier.verify(msg, &sig1).expect("PSS sig1 must verify");
    verifier.verify(msg, &sig2).expect("PSS sig2 must verify");
}
